export function printFullNameAndAddress(name: string, address: string) {
  return `Namaku adalah ${name} dan aku tinggal di ${address}`;
}

export function addTwo(num: number) {
  return num + 2;
}

export function subtractTwo(num: number) {
  return num - 2;
}

export default function divideTwo(num: number) {
  return num / 2;
}
